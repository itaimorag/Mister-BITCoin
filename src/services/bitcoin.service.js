import Axios from 'axios'
import { storageService } from './storage.service.js'

const STORAGE_KEY = 'marketPriceCache'
export const bitcoinService = {
  getRate,
  getMarketPrice,
  getConfirmedTransactions
}

let marketPriceCache = storageService.load(STORAGE_KEY) || {}
async function getRate(coins) {
  return await Axios.get(`https://blockchain.info/tobtc?currency=USD&value=${coins}`)

}

async function getMarketPrice() {
  if (marketPriceCache['marketPrice']) return Promise.resolve(marketPriceCache['marketPrice'])
  const {data} =await Axios.get(`https://api.blockchain.info/charts/market-price?timespan=1months&format=json&cors=true`)
            marketPriceCache['marketPrice'] = data
            storageService.store(STORAGE_KEY, marketPriceCache)
              return data

// return markerPrice.values
}

async function getConfirmedTransactions() {
  if (marketPriceCache['confirmedTransactions']) return Promise.resolve(marketPriceCache['confirmedTransactions'])
  const {data} =await Axios.get(`https://api.blockchain.info/charts/transactions-per-second?timespan=2weeks&rollingAverage=8hours&format=json&cors=true`)
            marketPriceCache['confirmedTransactions'] = data
            storageService.store(STORAGE_KEY, marketPriceCache)
              return data
  // return transitionPerSec.values
}




